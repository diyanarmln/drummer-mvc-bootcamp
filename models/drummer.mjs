// CREATES THE MODEL FOR THE TABLES_ we want in psql.
// This determines the columns and datatypes
export default function initDrummerModel(sequelize, DataTypes) {
  return sequelize.define(
    'drummer',
    {
      id: {
        allowNull: false,
        autoIncrement: true,
        primaryKey: true,
        type: DataTypes.INTEGER,
      },
      name: {
        type: DataTypes.STRING,
      },
      createdAt: {
        allowNull: false,
        type: DataTypes.DATE,
      },
      updatedAt: {
        allowNull: false,
        type: DataTypes.DATE,
      },
    },
    {
      underscored: true,
    },
  );
}
